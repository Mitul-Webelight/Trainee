// --------------- var

var a = 5;

var b = 10;

function variableVar() {
  a = 6;
  b = 11;
  console.log(a);
  console.log(b);
}
console.log(a);
console.log(b);
variableVar();

// --------------- let
let a = 5;

let b = 10;

function variableLet() {
  a = 6;
  b = 11;
  console.log(a);
  console.log(b);
}
console.log(a);
console.log(b);
variableLet();

